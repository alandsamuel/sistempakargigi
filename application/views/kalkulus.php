    <h1>Calculus (Karang Gigi)</h1>
    <hr>
    <h3>Apa Itu Calculus??</h3>
    <h4>Calculus atau Karang gigi adalah kumpulan plak gigi dan sisa-sisa makanan yang tidak dibersihkan dalam waktu lama sehingga mengalami pengerasan. Warnanya bisa coklat, hijau, putih, maupun kuning dan biasa terlihat melingkar di perbatasan gigi dan gusi.<br><br>
    Karang gigi ada dua macam yakni supra gingival dan sub gingival. Kalkulus supra gingival terletak di atas gusi atau di permukaan gigi yang tampak di rongga mulut. Sedangkan kalkulus subgingiva terletak di bawah gusi atau di permukaan akar gigi, sehingga tidak tampak di rongga mulut. </h4>
    
