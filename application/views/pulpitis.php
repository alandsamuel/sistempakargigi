    <h1>Pulpitis</h1>
    <hr>
    <h3>Apa yang dimaksud dengan pulpitis ?</h3>
    <h4>Pulpitis merupakan peradangan yang terjadi di pulpa, yakni bagian gigi paling dalam yang terdapat saraf dan pembuluh darah. Kondisi ini bisa menyebabkan munculnya nyeri yang luar biasa. Pulpitis merupakan salah satu masalah gigi yang cukup banyak dialami oleh masyarakat Indonesia adalah peradangan pada pulpa gigi yang menimbulkan rasa nyeri.  Jika seseorang mengalami pulpitis tentu saja rasa nyeri yang sangat yang dapat dirasakan dan jika tidak segera ditangani maka bisa saja menimbulkan peradangan dan bahkan bisa menimbulkan komplikasi yang lebih serius lagi.</h4>
    <br>
    <h3>Penyebab :</h3>
    <h4>1. Penyebab pulpitis yang paling sering ditemukan adalah pembusukan gigi.<br>
    2. Cedera.<br>
    (Pulpa terbungkus dalam dinding yang keras sehingga tidak memiliki ruang yang cukup untuk membengkak ketika terjadi peradangan. Yang terjadi hanyalah peningkatan tekanan di dalam gigi. Peradangan yang ringan, jika berhasil diatasi, tidak akan menimbulkan kerusakan gigi yang permanen. Peradangan yang berat bisa mematikan pulpa. Meningkatnya tekanan di dalam gigi bisa mendorong pulpa melalui ujung akar, sehingga bisa melukai tulang rahang dan jaringan di sekitarnya).</h4><br>
    <h3>Diagnosa</h3>
    <h4>Diagnosis ditegakkan berdasarkan gejala dan hasil pemeriksaan fisik. Untuk menentukan apakah pulpa masih bisa diselamatkan, bisa dilakukan beberapa pengujian :</h4>
    <i class="fa fa-angle-right" aria-hidden="true" style="font-size:18px;">&nbsp;&nbsp;Diberikan Rangsangan Dingin</i>
    <h4>Jika setelah rangsangan dihentikan nyerinya hilang, berarti pulpa masih sehat. Pulpa bisa dipertahankan dengan cara mencabut bagian gigi yang membusuk dan menambalnya. Jika nyeri tetap ada meskipun rangsangan dingin telah dihilangkan atau jika nyeri timbul secara spontan, maka pulpa tidak dapat dipertahankan. </h4>
    <i class="fa fa-angle-right" aria-hidden="true" style="font-size:18px;">&nbsp;&nbsp;Penguji Pulpa Elektrik</i>
    <h4>Alat ini digunakan untuk menunjukkan apakah pulpa masih hidup, bukan untuk menentukan apakah pulpa masih sehat. Jika penderita merasakan aliran listrik pada giginya, berarti pulpa masih hidup.</h4>
    <i class="fa fa-angle-right" aria-hidden="true" style="font-size:18px;">&nbsp;&nbsp;Menepuk Gigi Dengan Sebuah Alat</i>
    <h4>Jika dengan pengetukan gigi timbul nyeri, berarti peradangan telah menyebar ke jaringan dan tulang di sekitarnya.</h4>
    <i class="fa fa-angle-right" aria-hidden="true" style="font-size:18px;">&nbsp;&nbsp;Rontgen Gigi</i>
    <h4>Dilakukan untuk memperkuat adanya pembusukan gigi dan menunjukkan apakah penyebaran peradangan telah menyebabkan pengeroposan tulang di sekitar akar gigi.</h4><br>
