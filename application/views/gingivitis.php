    <h3>Apa Itu Gingivitis?</h3>
    <h4>Gingivitis adalah suatu kelainan berupa peradangan pada gusi. Gingivitis juga merupakan suatu bentuk dari penyakit periodontal. Penyakit periodontal terjadi ketika inflamasi dan infeksi menghancurkan jaringan yang menghancurkan gigi, termasuk gusi, ligamen periodontal, soket gigi (tulang alveolar). Gingivitis disebabkan oleh efek jangka panjang dari penumpukan plak dan bakteri. Plak adalah sutu materi yang melekat yang terbentuk di sekitar gigi karena bakteri, saliva dan sisa makanan. Sebagian besar kasus radang gusi, termasuk yang tingkat keparahannya ringan atau sedang, sebaiknya diobati sesegera mungkin. Radang gusi yang dibiarkan begitu saja berpotensi berkembang menjadi periodontitis, yaitu inflamasi pada jaringan pengikat di dalam gusi dan pada tulang di sekitar gigi, yang umumnya berujung pada gigi tanggal.</h4><br>
