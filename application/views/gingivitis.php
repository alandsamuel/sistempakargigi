    <i class="fa fa-user-md" aria-hidden="true" style="font-size:23px;">Pengobatan</i>
<h4>Pengobatan gingivitis dapat dilakukan oleh dokter gigi berdasarkan pada penyebab yang menjadi pemicu munculnya gingivitis, sehingga dalam melakukan penanganan gingivitis ini dapat dilakukan dengan benar dan tepat. Tujuan dari perawatan ini adalah untuk mengurangi terjadinya peradangan atau inflamasi. Perawatan yang dapat dilakukan oleh dokter untuk memperbaiki kondisi gingivitis, antara lain:<br><br>
1. Jika gingivitis disebabkan oleh plak dan karang gigi, maka dokter akan mengambil tindakan untuk membersihkan plak dan karang gigi dengan melakukan prosedur scalling. Setelah melakukan scalling, biasanya pasien diminta untuk kembali agar dapat dipantau apakah gusi telah dalam perbaikan atau belum akibat gingivitis.<br><br>
2. Apabila gingivitis yang muncul karena gigi yang berlubang, maka dokter akan melakukan penambalan dan memperbaiki kondisi gigi tersebut, sehingga kesehatan pada gigi akan tetap terjaga. Sementara jika gingivitis terjadi karena pengaruh dari pemakain tembakau, maka dokter akan meminta pasien untuk menghentikannya.<br><br>
    3. Gingivitis yang diakibatkan dari pengaruh penyakit lain seperti diabetes, maka dokter akan merujuk pada dokter spesialis ahli penyakit dalam untuk menanganinya. Pada sebagian kasus gingivitis kemungkinan diperlukan pemberian antibiotik yang harus diresepkan oleh dokter gigi. Tujuan pemberian antibiotik adalah untuk membunuh bakteri penyebab gingivitis.</h4><br>
