<h2>Prinsip Utama Menjaga Kesehatan Gigi dan Mulut</h2>
<hr> 
        <center><h3><i class="fa fa-clock-o" aria-hidden="true" style="font-size:21px;"> Waktu</i></h3></center>
<h4> Meskipun tidak ada aturan baku, akan tetapi berdasarkan informasi kesehatan waktu yang paling tepat untuk menggosok gigi adalah sebelum tidur dan setelah bangun tidur. Adapun alasan yang penting untuk kita ketahui bersama mengenai hal ini adalah apabila kita melakukan sikat gigi sebelum tidur, sisa-sisa makanan yang menempel pada gigi akan dapat di bersihkan semua sehingga tidak terjadi plak dan karang gigi.Bukan hanya itu saja, ketika tidur dalam keadaan mulut bersih akan menjaga kesehatan gigi dan gusi dari serangan berbagai jenis penyakit, sebut sajalah sakit gigi atau gigi berlubang, gingivitis, bau mulut, kanker gigi, gusi bengkak dan lain sebagainya
<br><br>
<center> <img src="<?php echo base_url(); ?>assets/img/waktu.png" heigh="180px" width="180px" /></center>
    <br><br>
Gigi kotor pada malam hari lebih rentan terserang penyakit dimana tidak ada antiseptic yang bekerja membunuh bakteri sehingga bakteri mudah berkembang biak dalam jumlah yang besar. Hal ini menjadikan resiko untuk terserang penyakit mulut menjadi lebih besar.Ketika pagi hari kita juga harus menggosok gigi agar dapat membersihkan bakteri yang berkembang biak saat tidur. Meskipun kita sudah menggosok gigi sebelum tidur, akan tetapi bakteri masih saja bisa berkembangbiak. Hal ini di sebabkan ketika kita tidur mulut menjadi kering sehingga bakteri mudah berkembang biak.</h4>
    <br><br>
<center><h3><i class="fa fa-wrench" aria-hidden="true" style="font-size:21px;"> Peralatan</i></h3></center>
<h4>Perlu kita perhatikan dalam memilih produk kesehatan untuk gigi. Berikut tips-tips dan penjelasannya :<br>

1.    Sikat gigi<br>
        <center> <img src="<?php echo base_url(); ?>assets/img/sikatgigilembut.png" heigh="200px" width="200px" /></center>
Pilihan terbaik memilih sikat gigi adalah bagian sikat yang lembut dan ujungnya tidak rata. Sikat yang lembut dan ujungnya tidak rata akan memudahkan masuk kedalam sela-sela gigi. Hal ini akan membersihkan gigi lebih maksimal dalam waktu yang singkat. Keuntungan lainnya yang di dapat dengan menggunakan sikat gigi yang lembut adalah melindungi email gigi agar tidak mudah terkikis, merawat gusi agar tidak luka atau iritasi, merawat langit-langit agar tidak luka, menyikat lebih banyak bagian gigi. Hindari memilih sikat yang keras, karena dapat mengikis email gigi lebih cepat serta dapat membuat iritasi pada gusi. Hal ini dapat mempercepat kerusakan organ yang ada di dalam mulut. 
<br><br><br>
2.    Pasta gigi<br>
        <center> <img src="<?php echo base_url(); ?>assets/img/odol.png" heigh="180px" width="180px" /></center><br>
Hal yang perlu di perhatikan di sini adalah kandungan yang ada di dalam pasta gigi yaitu flouride.

Flouride merupakan mineral yang sangat bermanfaat untuk kesehatan gigi. Ketika kita mengkonsumsi bahan makanan yang banyak mengandung gula dan pati, menyebabkan bakteri mudah berkembang biak di gigi. Disinilah peran dari Flouride akan membantu melindungi gigi dari bakteri tersebut.

Manfaat fluoride pada gigi kita adalah melindungi gigi dari asam di dalam mulut. Asam ini bisa di sebabkan oleh makanan, ataupun dari bakteri yang berkembangbiak dimulut. Sisa-sisa makanan yang tertinggal di sela-sela gigi kita juga dapat di urai bakteri menjadi asam. Hal ini akan menambah cepat rusaknya gigi. Nah fluoride akan membuat enamel gigi menjadi lebih kuat dan memberikan melindungi dari sengatan asam.<br><br><br>

3.    Obat kumur<br>
         <center> <img src="<?php echo base_url(); ?>assets/img/obatkumur5.png" heigh="70px" width="70px" /></center><br>
Sebagian kita juga pernah menggunakan obat kumur, sebagian juga tidak. Memang ada banyak obat kumur yang ada di pasaran. Dalam merawat gigi tidak ada salahnya untuk menggunakan obat kumur ini agar dapat membersihkan gigi 2 kali lipat. Hal ini disebabkan karena dengan berkumur dapat menjangkau seluruh bagian sela-sela gigi sehingga dapat memberikan keberihan mulut optimal.

Adapun kandungan yang perlu di perhatikan dalam obat kumur adalah antibakteri, fluoride, tidak mengandung pewarna dan mengandung penyegar mulut atau rasa mint. 
  <br><br><br>
4.    Flossing dan water pick<br>
     <center> <img src="<?php echo base_url(); ?>assets/img/floss.png" heigh="180px" width="180px" />
     <img src="<?php echo base_url(); ?>assets/img/waterdua.png" heigh="160px" width="160px" /></center><br>
    Gunakan floosing (benang gigi) dan water pick (semprotan air) agar benar-benar tidak sisa makanan di sela-sela gigi.</h4><br><br>
        
        <center><h3><i class="fa fa-user" aria-hidden="true" style="font-size:21px;"> Pola Hidup</i></h3></center><br>
<h4>1. Jangan merokok<br>
        <center> <img src="<?php echo base_url(); ?>assets/img/nosmokingdua.png" heigh="130px" width="130px" /></center><br>
Tembakau dapat menyebabkan gigi menguning dan bibir menghitam. Merokok juga melipatgandakan risiko Anda terhadap penyakit gusi dan kanker mulut. Oleh karena itu, berhenti merokok sekarang juga.
<br><br><br><br>
2. Minum lebih banyak air
        <center> <img src="<?php echo base_url(); ?>assets/img/botol1.png" heigh="130px" width="130px" /></center><br>
Air merupakan minuman terbaik untuk kesehatan Anda secara keseluruhan, termasuk bagi kesehatan mulut Anda karena aktivitas minum dapat membantu membersihkan beberapa efek negatif dari makanan dan minuman yang menempel pada gigi Anda.
<br><br><br><br>
3.  Batasi konsumsi makanan yang manis dan asam
        <center> <img src="<?php echo base_url(); ?>assets/img/nocandy4.png" heigh="130px" width="130px" /></center><br>
Anda mungkin seringkali mendengar nasihat, “Jangan banyak makan makanan manis, nanti giginya bolong”. Ternyata, kita memang tidak boleh sembarangan membantah nasehat orangtua. Makanan manis dan asam akan diubah menjadi asam oleh bakteri di mulut yang kemudian dapat menggerogoti enamel gigi Anda. Asam inilah yang menyebabkan gigi Anda cepat berlubang.

Tidak perlu menghentikan konsumsi gula sama sekali untuk menjaga kesehatan gigi dan mulut, Anda hanya perlu membatasi konsumsinya.
<br><br><br><br>
        
4. Makan makanan yang bergizi<br>
<center> <img src="<?php echo base_url(); ?>assets/img/makananbergizidua.png" heigh="150px" width="150px" /></center><br>

Sama halnya dengan air, makan makanan yang bergizi juga baik untuk kesehatan gigi dan mulut Anda. Makan makanan yang bergizi — termasuk biji-bijian, kacang-kacangan, buah-buahan dan sayuran, dan produk susu — dapat memberikan semua nutrisi yang Anda butuhkan. Bahkan, sebuah studi menemukan bahwa omega-3 lemak — jenis lemak sehat dalam makanan laut— dapat dapat mengurangi risiko peradangan, sehingga dapat menurunkan risiko penyakit gusi.
<br><br><br><br>
        
5. Kontrol ke dokter gigi setiap 6 bulan sekali 
<center> <img src="<?php echo base_url(); ?>assets/img/doktergigi.webp" heigh="200px" width="180px" /></center><br>
    Mengontrol gigi setiap enam bulan sekali dapat menjaga kesehatan gigi dan rongga mulut. Dokter gigi akan membersihkan jenis makanan yang masih mengendap di gigi agar dapat mencegah terjadi penyakit gigi. </h4></div>
