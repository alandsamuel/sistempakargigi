    <h1>Radang Mukosa Mulut (Sariawan)</h1>
    <hr>
    <h3>Apa Itu Radang Mukosa Mulut (Sariawan) ?</h3>
    <h4>Sariawan merupakan radang yang terjadi pada mukosa (pipi bagian dalam) mulut, biasanya berupa bercak putih kekuningan. Bercak itu dapat berupa bercak tunggal maupun berkelompok. Sariawan dapat menyerang selaput lendir pipi bagian dalam, bibir bagian dalam, lidah, gusi, serta langit-langit dalam rongga mulut. Meskipun tidak tergolong berbahaya, tapi sariawan sangat menganggu.
Penyakit ini tergolong penyakit ringan, hanya saja akan mengurangi selera makan, lantaran rasa perih yang dialami akibat sentuhan makanan pada sariawannya. Umumnya lokasi sariawan di bibir, lidah, pipi bagian dalam (mukosa), dan tenggorokan, jarang terjadi pada gusi.</h4>
    <h3>Apa Penyebab Stomatitis?</h3>
    <h4>1. Daya tahan tubuh yang rendah<br>
        2. Kondisi mulut seperti kebersihan mulut yang buruk, luka pada mulut karena makanan atau minuman yang terlalu panas <br>
        3. Kondisi tubuh seperti adanya alergi atau infeksi <br>
        4. Luka akibat menyikat gigi terlalu keras atau bulu sikat gigi yang sudah mengembang<br>
        5. Kekurangan vitamin C dan vitamin B<br>
    </h4>
   
    <h3>Bagaimana Mencegah Stomatitis? </h3>
<h4>
•	Usahakan untuk selalu menjaga kebersihan mulut dan gigi <br>
•	Banyak mengkonsumsi makanan yang mengandung vitamin C dan vitamin B, seperti buah-buahan dan sayur-sayuran.<br> 
•	Hati-hati saat menggosok gigi agar tidak menimbulkan luka didalam mulut<br>
    • Hindari memakan makanan yang terlalu pedas </h4>
