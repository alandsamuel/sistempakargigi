    <h3>Apa yang dimaksud dengan abses gigi?</h3>
    <h4>Abses gigi adalah sekumpulan nanah yang disebabkan oleh infeksi bakteri pada bagian dalam gigi. Kondisi ini biasanya muncul sebagai akibat dari rongga gigi tidak diobati, adanya retakan atau serpihan pada gigi yang memungkinkan bakteri masuk ke dalam gigi bagian dalam. Abses dapat terjadi pada berbagai daerah gigi karena berbagai alasan. Abses periapical terjadi pada ujung akar, sedangkan abses periodontal terjadi di gusi sebelah akar gigi.<br><br>
    Dokter gigi akan mengobati abses gigi dengan mengeringkan dan menyingkirkan infeksi. Dokter dapat mengobati abses gigi dengan perawatan saluran akar atau pencabutan gigi. Abses gigi yang tidak diobati dapat menimbulkan komplikasi yang mengancam jiwa. Abses gigi yang tidak diobati dapat menyebabkan komplikasi serius, bahkan mengancam jiwa. </h4><br>
    <h3>Penyebab :</h3>
    <h4>1. Abses gigi ini terjadi dari infeksi gigi yang berisi cairan (nanah) dialirkan ke gusi sehingga gusi yang berada di dekat gigi tersebut membengkak.<br>
     2. Abses gigi terjadi ketika bakteri menyerang pulpa gigi pada bagian (lembut) paling dalam dari gigi yang berisi pembuluh darah, saraf, dan jaringan ikat. Bakteri masuk melalui rongga gigi atau serpihan atau retakan pada gigi dan menyebar sampai ke akar. Infeksi bakteri menyebabkan pembengkakan dan peradangan. Ruang sempit di mana peradangan terjadi mendorong nanah agar masuk ke dalam saku (abses) di ujung akar.</h4><br>
