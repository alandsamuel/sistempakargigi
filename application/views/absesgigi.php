<i class="fa fa-user-md" aria-hidden="true" style="font-size:19px;">&nbsp;&nbsp;Perawatan dan Pengobatan :</i><br>
<h4>Tujuan dari pengobatan adalah untuk menghilangkan infeksi. Untuk mencapai hal ini, dokter gigi Anda mungkin akan:<br>
1. Membuka dan mengeringkan abses 
<br>Dokter gigi akan membuat potongankecil keabses untuk membuat nanah keluar, dan kemudian area dicuci dengan garam (saline).<br><br>
2. Melakukan kanal akar <br> 
Prosedur ini dapat membantu menghilangkan infeksi dan menyelamatkan gigi Anda. Untukmelakukan hal ini, dokter akan mengebor gigi dan menghilangkan jaringan pusat yang sakit (pulp) dan saluran air abses. Dokter kemudian mengisi dan menyegel ruang pulpa dan saluran akar gigi, serta menutup gigi dengan untuk meningkatkan kekuatan gigi, terutama untuk gigi geraham.<br><br>
3. Mencabut gigi<br> Jika gigi yang terinfeksi tidak bisa diselamatkan, dokter Anda mungkin akan mencabut gigi dan mengeringkan abses untuk menyingkirkan infeksi.<br><br>
4. Meresepkan antibiotik <br> Jika infeksi hanya terjadi pada daerah bengkak, Anda mungkin tidak perlu antibiotik. Tapi jika infeksi telah menyebar ke gigi di dekatnya, rahang atau daerah lain, dokter gigi Anda akan cenderung meresepkan antibiotik untuk menghentikannya dari penyebaran lebih lanjut. Dokter juga merekomendasikan antibiotik jika Anda memiliki sistem kekebalan tubuh yang lemah.<br><br>
Pada masa penyembuhan, dokter gigi mungkin akan menyarankan beberapa tips untuk mengurangi ketidaknyamanan:<br>
1. Berkumur dengan air garam hangat.<br>
2. Memakai obat penghilang rasa sakit, sepertiacetaminophen(Tylenol) danibuprofen(Advil, MotrinIB) sesuai kebutuhan.<br><br>
